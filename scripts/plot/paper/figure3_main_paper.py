#!/usr/bin/env python3
"""
Create Figure 3 (main paper): temporal drift details in a single-column, 2-panel vertical layout.

This figure reproduces the two metrics shown in:
  outputs/evaluation/sigmoid/period4/single_k/no_budget/plots/panel_a_lineplot.png

but reformats them into a vertical stack and uses the same paper styling as Figures 1/4/5/6/7.

Data sources (generated by `scripts/plot/eval_sigmoid.py`):
  - OOD coverage error (mean(hat_tau - tau)) from:
      outputs/evaluation/sigmoid/period4/single_k/no_budget/k{1,2,3}/bins/*_bins_test_overlap.csv
  - OOD pinball loss proxy (mean(abs_err)) from:
      evaluation_pinball/period4_singlek_no_budget/k{1,2,3}/bins/*_bins_test_overlap.csv

Outputs:
  - outputs/figures_main_paper/figure3_main_paper.pdf
  - outputs/figures_main_paper/figure3_main_paper.png
"""

from __future__ import annotations

import argparse
import os
import re
import sys
from dataclasses import dataclass
from typing import Dict, List, Optional, Sequence, Tuple

import numpy as np

try:
    import pandas as pd  # type: ignore
except Exception as e:  # pragma: no cover
    raise SystemExit("This script requires pandas.") from e

try:
    import matplotlib.pyplot as plt  # type: ignore
    from matplotlib.ticker import FuncFormatter  # type: ignore
except Exception as e:  # pragma: no cover
    raise SystemExit("This script requires matplotlib.") from e

# Import bootstrap from `scripts/` by putting the scripts dir on sys.path.
SCRIPTS_DIR = os.path.abspath(os.path.join(os.path.dirname(__file__), os.pardir, os.pardir))
if SCRIPTS_DIR not in sys.path:
    sys.path.insert(0, SCRIPTS_DIR)
from _bootstrap import ensure_repo_root_on_path  # type: ignore

REPO_ROOT = ensure_repo_root_on_path(__file__)

from skill_frontier.plotting.plot_utils import ensure_dir, save_figure  # noqa: E402
from skill_frontier.plotting.plot_paths import FIGURES_MAIN_PAPER_DIR  # noqa: E402
from skill_frontier.plotting.paper_style import apply_rcparams, STYLE_FIGURE3, TITLE_BBOX_MAIN_PAPER  # noqa: E402
from skill_frontier.plotting.panel_style import apply_panel_style  # noqa: E402
from skill_frontier.io.output_paths import sanitize_task_name  # noqa: E402
from skill_frontier.io.task_mappings import MAIN_TASKS_FIG3_ORDER  # noqa: E402


# -----------------------------------------------------------------------------
# CONFIG (single-column vertical layout)
# -----------------------------------------------------------------------------

# Horizontal 1×2 layout: keep width <= 2× height.
FIG_HEIGHT = 3.0
FIG_WIDTH = 6.7  # ratio = 2.0
DPI = 300

SUBPLOT_ADJUST = dict(left=0.10, right=0.98, bottom=0.16, top=0.92, wspace=0.3)

TAU = 0.98

TASKS: Sequence[str] = tuple(MAIN_TASKS_FIG3_ORDER)

DRIFT_TASKS = {"IFEval Raw", "MATH Lvl 5 Raw", "MMLU-PRO Raw"}

# ColorBrewer Set1-ish palette (consistent with other figures using the same family).
TASK_COLORS: Dict[str, str] = {
    "BBH Raw": "#377eb8",  # blue
    "GPQA Raw": "#ff7f00",  # orange
    "IFEval Raw": "#4daf4a",  # green
    "MATH Lvl 5 Raw": "#e41a1c",  # red
    "MMLU-PRO Raw": "#984ea3",  # purple
    "MUSR Raw": "#a65628",  # brown
}

STYLE = STYLE_FIGURE3

TITLE_BBOX = TITLE_BBOX_MAIN_PAPER


def _configure_rcparams() -> None:
    apply_rcparams("figure3")


def _sanitize_task_name(task: str) -> str:
    return sanitize_task_name(task)


def _task_legacy_filename(task: str) -> str:
    # Some older scripts used a space-preserving name with underscores for spaces.
    return task.replace(" ", "_")


def _find_bins_path(eval_dir: str, task: str, which: str) -> Optional[str]:
    task_clean = _sanitize_task_name(task)
    task_legacy = _task_legacy_filename(task)
    era_candidates = ["train"] if which == "train" else ["test_fixed", "test_overlap", "test"]

    candidate_dirs: List[str] = []
    bins_dir = os.path.join(eval_dir, "bins")
    if os.path.isdir(bins_dir):
        candidate_dirs.append(bins_dir)
    candidate_dirs.append(eval_dir)

    for era in era_candidates:
        for d in candidate_dirs:
            candidates = [
                os.path.join(d, f"{task_clean}_bins_{era}.csv"),
                os.path.join(d, f"{task_legacy}_bins_{era}.csv"),
                os.path.join(d, f"{task}_bins_{era}.csv"),
            ]
            if which != "train" and era != "test_overlap":
                candidates.extend(
                    [
                        os.path.join(d, f"{task_clean}_bins_test_overlap.csv"),
                        os.path.join(d, f"{task_legacy}_bins_test_overlap.csv"),
                        os.path.join(d, f"{task}_bins_test_overlap.csv"),
                    ]
                )
            for p in candidates:
                if os.path.isfile(p):
                    return p
    return None


def _list_k_eval_dirs(base_dir: str) -> List[Tuple[int, str]]:
    out: List[Tuple[int, str]] = []
    for k in (1, 2, 3):
        kdir = os.path.join(base_dir, f"k{k}")
        if os.path.isdir(kdir):
            out.append((k, kdir))
    return out


def _mean_oos_metric(eval_dir: str, task: str, *, metric: str) -> float:
    """
    Read the bins CSV for OOS and compute the mean metric.

    metric:
      - "coverage_signed": mean(hat_tau - tau)
      - "pinball": mean(abs_err)
    """
    path = _find_bins_path(eval_dir, task, which="test")
    if not path:
        return float("nan")
    df = pd.read_csv(path)
    if df.empty:
        return float("nan")
    if metric == "coverage_signed":
        if "hat_tau" not in df.columns:
            return float("nan")
        return float((pd.to_numeric(df["hat_tau"], errors="coerce") - TAU).mean())
    if metric == "pinball":
        if "abs_err" not in df.columns:
            return float("nan")
        return float(pd.to_numeric(df["abs_err"], errors="coerce").mean())
    raise ValueError(f"Unknown metric: {metric}")


@dataclass(frozen=True)
class SeriesData:
    k_vals: List[int]
    per_task: Dict[str, np.ndarray]


def _load_series(base_dir: str, *, metric: str, tasks: Sequence[str]) -> SeriesData:
    k_dirs = _list_k_eval_dirs(base_dir)
    if not k_dirs:
        raise FileNotFoundError(f"No k* eval dirs found under: {base_dir}")
    k_vals = [k for (k, _d) in k_dirs]
    eval_dirs = [d for (_k, d) in k_dirs]
    out: Dict[str, np.ndarray] = {}
    for task in tasks:
        out[task] = np.array([_mean_oos_metric(ed, task, metric=metric) for ed in eval_dirs], dtype=float)
    return SeriesData(k_vals=k_vals, per_task=out)


def _scaled_formatter(scale: float) -> FuncFormatter:
    def _fmt(y: float, _pos: float) -> str:
        v = y / scale
        if abs(v) < 0.05:
            v = 0.0
        return f"{v:.1f}"

    return FuncFormatter(_fmt)


def _plot_panel(
    ax: plt.Axes,
    *,
    series: SeriesData,
    tasks: Sequence[str],
    title: str,
    ylabel: str,
    yscale: float,
) -> None:
    x = np.asarray(series.k_vals, dtype=float)
    for task in tasks:
        y = series.per_task.get(task)
        if y is None:
            continue
        is_drift = task in DRIFT_TASKS
        ax.plot(
            x,
            y,
            color=TASK_COLORS.get(task, "#333333"),
            marker="o",
            markersize=STYLE["marker_size_drift"] if is_drift else STYLE["marker_size"],
            linewidth=STYLE["line_width_drift"] if is_drift else STYLE["line_width"],
            alpha=STYLE["alpha_drift"] if is_drift else STYLE["alpha"],
            label=task,
            zorder=3,
        )

    ax.set_title(title, fontweight="bold", pad=8).set_bbox(TITLE_BBOX)
    ax.set_xticks(series.k_vals)
    ax.set_xlim(min(series.k_vals) - 0.1, max(series.k_vals) + 0.1)
    ax.set_ylabel(ylabel, fontsize=14, fontweight="bold", labelpad=4)
    ax.yaxis.set_major_formatter(_scaled_formatter(yscale))


def main() -> None:
    ap = argparse.ArgumentParser(description="Create Figure 3 (temporal drift) for the main paper.")
    ap.add_argument(
        "--calib_base",
        default=os.path.join(REPO_ROOT, "outputs", "evaluation", "sigmoid", "period4", "single_k", "no_budget"),
        help="Base directory for calibration evaluation outputs (coverage error).",
    )
    ap.add_argument(
        "--pinball_base",
        default=os.path.join(REPO_ROOT, "evaluation_pinball", "period4_singlek_no_budget"),
        help="Base directory for pinball evaluation outputs (pinball loss).",
    )
    ap.add_argument(
        "--out_dir",
        default=os.path.join(REPO_ROOT, str(FIGURES_MAIN_PAPER_DIR)),
        help="Output directory.",
    )
    args = ap.parse_args()

    _configure_rcparams()

    series_cov = _load_series(args.calib_base, metric="coverage_signed", tasks=TASKS)
    series_pin = _load_series(args.pinball_base, metric="pinball", tasks=TASKS)

    # Align k across the two bases (should be identical).
    common_k = [k for k in series_cov.k_vals if k in series_pin.k_vals]
    if not common_k:
        raise SystemExit("No overlapping k values between calibration and pinball bases.")
    if common_k != series_cov.k_vals or common_k != series_pin.k_vals:
        # Reindex if needed (rare).
        idx_cov = [series_cov.k_vals.index(k) for k in common_k]
        idx_pin = [series_pin.k_vals.index(k) for k in common_k]
        series_cov = SeriesData(
            k_vals=common_k,
            per_task={t: v[idx_cov] for t, v in series_cov.per_task.items()},
        )
        series_pin = SeriesData(
            k_vals=common_k,
            per_task={t: v[idx_pin] for t, v in series_pin.per_task.items()},
        )

    fig, axes = plt.subplots(1, 2, figsize=(FIG_WIDTH, FIG_HEIGHT), dpi=DPI)
    plt.subplots_adjust(**SUBPLOT_ADJUST)

    for ax in axes:
        apply_panel_style(ax, STYLE)

    _plot_panel(
        axes[0],
        series=series_cov,
        tasks=TASKS,
        title="(a) OOD Coverage Error",
        ylabel=r"$\mathbf{mean}\,\hat{\tau}-\tau\;(\times 10^{-2})$",
        yscale=1e-2,
    )
    _plot_panel(
        axes[1],
        series=series_pin,
        tasks=TASKS,
        title="(b) OOD Pinball Loss",
        ylabel=r"$\mathbf{mean}\,\rho_{\tau}\;(\times 10^{-3})$",
        yscale=1e-3,
    )

    # X-axis formatting: both panels show tick labels and xlabel.
    for ax in axes:
        ax.set_xlabel(r"Period $t$", fontsize=15, fontweight="bold", labelpad=4)

    # Legend inside the second subplot (top-left).
    handles, labels = axes[1].get_legend_handles_labels()
    axes[1].legend(
        handles,
        labels,
        loc="upper right",
        frameon=True,
        framealpha=0.95,
        edgecolor="#c0c0c0",
        fancybox=False,
        handlelength=1.6,
        handletextpad=0.5,
        columnspacing=0.9,
        borderpad=0.4,
        fontsize=10,
    )

    ensure_dir(args.out_dir)
    out_base = os.path.join(args.out_dir, "figure3_main_paper")
    save_figure(
        fig,
        args.out_dir,
        "figure3_main_paper",
        dpi=DPI,
        bbox_inches="tight",
        pad_inches=0.05,
        facecolor="white",
        edgecolor="none",
    )
    plt.close(fig)
    print(f"Saved: {out_base}.pdf and {out_base}.png")


if __name__ == "__main__":
    main()
